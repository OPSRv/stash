use super::caches::dir_size;
use super::cancel;
use serde::Serialize;
use std::path::Path;
use walkdir::WalkDir;

#[derive(Debug, Clone, Serialize, PartialEq)]
pub struct NodeModulesEntry {
    pub path: String,
    /// Parent project directory — the one that holds `package.json`.
    pub project: String,
    pub size_bytes: u64,
    /// UNIX seconds of last access (mtime as a proxy — atime isn't reliably
    /// updated on macOS when mounted with `noatime`).
    pub last_modified: i64,
}

/// Walk `root` breadth-first, emitting one entry per `node_modules` directory
/// found. We deliberately do NOT descend into a `node_modules` once found —
/// otherwise a monorepo with 50 workspaces would produce thousands of nested
/// results dominated by transitive deps.
pub fn scan(root: &Path) -> Vec<NodeModulesEntry> {
    cancel::reset("node_modules");
    let mut out = Vec::new();
    let walker = WalkDir::new(root).follow_links(false).into_iter();
    let mut it = walker.filter_entry(|e| {
        // Skip hidden / DS_Store-style entries but never the root itself —
        // tempfile::tempdir() is happy to hand us ".tmpXXXX" paths, and the
        // user might point us at a hidden project dir deliberately.
        if e.depth() == 0 {
            return true;
        }
        let name = e.file_name().to_string_lossy();
        !name.starts_with('.')
    });
    while let Some(entry) = it.next() {
        if cancel::is_cancelled("node_modules") {
            break;
        }
        let entry = match entry {
            Ok(e) => e,
            Err(_) => continue,
        };
        if !entry.file_type().is_dir() {
            continue;
        }
        if entry.file_name() == "node_modules" {
            let path = entry.path().to_path_buf();
            let project = path
                .parent()
                .map(|p| p.to_string_lossy().into_owned())
                .unwrap_or_default();
            let size_bytes = dir_size(&path);
            let last_modified = path
                .metadata()
                .and_then(|m| m.modified())
                .ok()
                .and_then(|t| t.duration_since(std::time::UNIX_EPOCH).ok())
                .map(|d| d.as_secs() as i64)
                .unwrap_or(0);
            out.push(NodeModulesEntry {
                path: path.to_string_lossy().into_owned(),
                project,
                size_bytes,
                last_modified,
            });
            // Skip descending into this node_modules.
            it.skip_current_dir();
        }
    }
    out.sort_by(|a, b| b.size_bytes.cmp(&a.size_bytes));
    out
}

#[cfg(test)]
mod tests {
    use super::*;
    use std::fs;

    #[test]
    fn scan_finds_top_level_node_modules_and_does_not_descend() {
        let tmp = tempfile::tempdir().unwrap();
        let proj = tmp.path().join("projA");
        let nm = proj.join("node_modules");
        let nested = nm.join("foo").join("node_modules");
        fs::create_dir_all(&nested).unwrap();
        fs::write(nm.join("top.bin"), vec![0u8; 4096]).unwrap();
        fs::write(nested.join("nested.bin"), vec![0u8; 8192]).unwrap();

        let entries = scan(tmp.path());
        assert_eq!(entries.len(), 1);
        assert!(entries[0].path.ends_with("projA/node_modules"));
        // Size includes nested content because dir_size walks recursively —
        // we just don't emit a SEPARATE entry for the nested node_modules.
        assert!(entries[0].size_bytes >= 4096 + 8192);
    }

    #[test]
    fn scan_skips_hidden_dirs() {
        let tmp = tempfile::tempdir().unwrap();
        let hidden = tmp.path().join(".cache").join("proj").join("node_modules");
        fs::create_dir_all(&hidden).unwrap();
        fs::write(hidden.join("a.bin"), vec![0u8; 1024]).unwrap();
        assert!(scan(tmp.path()).is_empty());
    }
}
